class AppLocalizations {
  const AppLocalizations(this.locale);

  final Locale locale;

  static const fallbackLocale = Locale('en');
  static const supportedLocales = [
    Locale.fromSubtags(languageCode: 'zh', scriptCode: 'Hans'),
    Locale.fromSubtags(languageCode: 'zh', scriptCode: 'Hant'),
    Locale('ja'),
    Locale('ko'),
    Locale('en'),
    Locale('fr'),
    Locale('es'),
    Locale('de'),
  ];

  static const LocalizationsDelegate<AppLocalizations> delegate =
      _AppLocalizationsDelegate();

  static bool isSupportedLocale(Locale locale) =>
      supportedLocales.any((supported) {
        if (supported.languageCode != locale.languageCode) return false;
        if (supported.scriptCode == null) return true;
        return supported.scriptCode == locale.scriptCode ||
            (supported.scriptCode == 'Hans' &&
                locale.languageCode == 'zh' &&
                locale.scriptCode == null);
      });

  static Locale resolve(Locale locale) {
    if (locale.languageCode == 'zh') {
      final isTraditional =
          locale.scriptCode == 'Hant' ||
          locale.countryCode == 'TW' ||
          locale.countryCode == 'HK' ||
          locale.countryCode == 'MO';
      return isTraditional
          ? const Locale.fromSubtags(languageCode: 'zh', scriptCode: 'Hant')
          : const Locale.fromSubtags(languageCode: 'zh', scriptCode: 'Hans');
    }
    return supportedLocales.firstWhere(
      (supported) => supported.languageCode == locale.languageCode,
      orElse: () => fallbackLocale,
    );
  }

  static AppLocalizations of(BuildContext context) =>
      Localizations.of<AppLocalizations>(context, AppLocalizations) ??
      const AppLocalizations(fallbackLocale);

  static String translate(BuildContext context, String source) =>
      of(context).t(source);

  String get _key {
    if (locale.languageCode == 'zh') {
      return locale.scriptCode == 'Hant' ||
              locale.countryCode == 'TW' ||
              locale.countryCode == 'HK' ||
              locale.countryCode == 'MO'
          ? 'zhHant'
          : 'zhHans';
    }
    return locale.languageCode;
  }

  String t(String source) {
    if (source.isEmpty) return source;
    final direct = _translations[_key]?[source];
    if (direct != null) return direct;
    final english = _translations['en']?[source];
    if (english != null) return english;
    return _formatDynamic(source) ?? source;
  }

  String format(String key, String value) => _format(key, value);

  String? _formatDynamic(String source) {
    final topicCount = RegExp(r'^(\d+) 个话题$').firstMatch(source);
    if (topicCount != null) {
      return _format('topicCount', topicCount.group(1)!);
    }
    final memberCount = RegExp(r'^(\d+)人$').firstMatch(source);
    if (memberCount != null) {
      return _format('memberCount', memberCount.group(1)!);
    }
    final newUpdates = RegExp(r'^(\d+) 条新动态$').firstMatch(source);
    if (newUpdates != null) {
      return _format('newUpdates', newUpdates.group(1)!);
    }
    final channelId = RegExp(r'^频道号：(.+)$').firstMatch(source);
    if (channelId != null) {
      return _format('topicId', channelId.group(1)!);
    }
    final version = RegExp(r'^版本 (.+)$').firstMatch(source);
    if (version != null) {
      return _format('version', version.group(1)!);
    }
    final leaveTopic = RegExp(r'^退出「(.+)」后将删除该话题频道。继续？$').firstMatch(source);
    if (leaveTopic != null) {
      return _format('leaveTopicMessage', leaveTopic.group(1)!);
    }
    return null;
  }

  String _format(String key, String value) {
    final template = _formats[_key]?[key] ?? _formats['en']![key]!;
    return template.replaceAll('{value}', value);
  }
}

extension LocalizedString on String {
  String l10n(BuildContext context) =>
      AppLocalizations.translate(context, this);
}

class _AppLocalizationsDelegate
    extends LocalizationsDelegate<AppLocalizations> {
  const _AppLocalizationsDelegate();

  @override
  bool isSupported(Locale locale) => AppLocalizations.isSupportedLocale(locale);

  @override
  Future<AppLocalizations> load(Locale locale) {
    final resolved = AppLocalizations.resolve(locale);
    Intl.defaultLocale = resolved.toLanguageTag();
    return SynchronousFuture(AppLocalizations(resolved));
  }

  @override
  bool shouldReload(covariant LocalizationsDelegate<AppLocalizations> old) =>
      false;
}

